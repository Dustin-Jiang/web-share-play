function getRandomInt(min: number, max: number) {
  min = Math.ceil(min);
  max = Math.floor(max);
  return Math.floor(Math.random() * (max - min)) + min; //不含最大值，含最小值
}


export default function getRandomPhrase() {
  let result = []

  for (let section of words) {
    result.push(section[getRandomInt(0, section.length)])
  }

  return result.join("-");
}